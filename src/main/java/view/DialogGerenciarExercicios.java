    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        List<AlunoProfessorExercicio> lista = new ArrayList();

        lista = gerInterGrafica.getGerDom().getAlProfExDao().listar(AlunoProfessorExercicio.class);

        Professor professorSelecionado = null;
        Aluno alunoSelecionado = null;
        Exercicio exercicioSelecionado = null;

        int indexProfessor = 0;
        int indexAluno = 0;
        int indexExercicio = 0;

        indexProfessor = jComboBoxProfessor.getSelectedIndex();
        indexAluno = jComboBoxAluno.getSelectedIndex();
        indexExercicio = jComboBoxExercicio.getSelectedIndex();

        if (indexProfessor == 0 || indexAluno == 0 || indexExercicio == 0) {
            JOptionPane.showMessageDialog(this, "ERRO AO ADICIONAR");

        } else {

            AlunoProfessorExercicioTableModel exercicioTable = new AlunoProfessorExercicioTableModel();

            professorSelecionado = (Professor) jComboBoxProfessor.getSelectedItem();
            alunoSelecionado = (Aluno) jComboBoxAluno.getSelectedItem();
            exercicioSelecionado = (Exercicio) jComboBoxExercicio.getSelectedItem();

            for (AlunoProfessorExercicio alPrEx : lista) {
                if (alPrEx.getChavePK().getAluno().getId() ==alunoSelecionado.getId() && alPrEx.getChavePK().getProfessor().getId() == professorSelecionado.getId() && alPrEx.getChavePK().getExercicio().getId() == exercicioSelecionado.getId()) {
                    JOptionPane.showMessageDialog(this, "Já existe uma atribuição para o ALUNO: " + alPrEx.getChavePK().getAluno().getNome() + ", PROFESSOR: " + alPrEx.getChavePK().getProfessor().getNome() + " e EXERCÍCIO: " + alPrEx.getChavePK().getExercicio().getNome() + ".", "Erro", JOptionPane.ERROR_MESSAGE);

                } else {
                    AlunoProfessorExercicioTableModel model = (AlunoProfessorExercicioTableModel) jTableCronogramaExercicios.getModel();

                    AlunoProfessorExercicio a1 = new AlunoProfessorExercicio(professorSelecionado, alunoSelecionado, exercicioSelecionado);
                    model.adicionar(a1);

                }
            }

        }

    }//GEN-LAST:event_jButtonAdicionarActionPerformed

