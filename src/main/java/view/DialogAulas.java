    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String nome = jTextFieldNomeAula.getText();
        String id = jFormattedTextFieldID.getText();
        int idInt = Integer.parseInt(id);

        List<Aulas> aulasGeneric = new ArrayList();
        Aulas aula = null;

        aulasGeneric = gerInter.getInstance().getGerDom().listar(Aulas.class);

        int existente = 0;
        for (Aulas aulaDaLista : aulasGeneric) {
            if (id.equals(aulaDaLista.getIdAula())) {
                aula = aulaDaLista;
                existente = 1;
            }
        }

        try {

            if (existente == 0) {
                gerDom.inserirAula(nome, idInt);
                JOptionPane.showMessageDialog(this, "Aula cadastrada com sucesso!");
                jTextFieldNomeAula.setText("");
                jFormattedTextFieldID.setText("");
            } else {
                gerDom.alterarAula(aula, idInt, nome);
                JOptionPane.showMessageDialog(this, "Aula alterada com sucesso!");
                jTextFieldNomeAula.setText("");
                jFormattedTextFieldID.setText("");
                this.dispose();

            }
        } catch (HibernateException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO ao cadastrar", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed


