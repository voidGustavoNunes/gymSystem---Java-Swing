    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        List<Aulas> lista = new ArrayList();

        lista = gerInter.getInstance().getGerDom().listar(Aulas.class);
        String id = (String) jFormattedTextFieldID.getValue();
        int idInt = Integer.parseInt(id);
        Aulas aula = null;

        for (Aulas aulaLista : lista) {
            if (aulaLista.getIdAula() == idInt) {
                aula = aulaLista;

                if (JOptionPane.showConfirmDialog(this, "Deseja excluir a aula de id " + id + "?", "Mensagem", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                    gerInter.getGerDom().excluir(aula);
                    JOptionPane.showMessageDialog(this, "Aula excluida com sucesso!");
                }

            } else {
                JOptionPane.showMessageDialog(this, "ERRO AO EXCLUIR AULA DE ID: " + id, "ERRO excluir", JOptionPane.ERROR_MESSAGE);

            }
        }


    }//GEN-LAST:event_jButtonExcluirActionPerformed

