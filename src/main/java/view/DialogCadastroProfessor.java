    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed

        Professor professor = null;
        int existente = 0;

        String codigoText = jFormattedTextFieldCodigo.getText();
        int codigo = Integer.parseInt(codigoText);

        List<Pessoa> listaPessoa = new ArrayList();

        listaPessoa = gerInterGrafica.getInstance().getGerDom().listar(Pessoa.class);

        //cria um aluno vazio para na primeira vez que for inserido, conseguir percorrer a lista abaixo sem estar vazia
        //senao ela irá parar nesse ponto e nao irá inserir o professor
        //como o aluno nao é inserido no banco, nao fará diferenca para o algoritmo em si
        Aluno aluno = new Aluno();
        listaPessoa.add(aluno);

        for (Pessoa pessoaSelecionado : listaPessoa) {
            if (pessoaSelecionado.getId() == codigo) {
                JOptionPane.showMessageDialog(
                        this, // Componente pai (null para um diálogo independente)
                        "Há uma pessoa cadastrada com esse código", // Mensagem de erro
                        "ERRO", // Título da janela
                        JOptionPane.ERROR_MESSAGE // Ícone de erro
                );
            } else {
                String senha = jTextFieldSenha.getText();
                Date dataCadastro = null;
                String dataCadastroText = jFormattedTextFieldDataCadastro.getText();
                try {
                    dataCadastro = formato.parse(dataCadastroText);

                } catch (ParseException e) {
                    e.printStackTrace();
                    System.out.println("\n======= PROBLEMA NA CONVERSÃO DA DATA DE CADASTRO =========\n");
                }
                String situacao = jComboBoxSituacao.getSelectedItem().toString();
                String usuarioMasterString = jComboBoxUsuarioMaster.getSelectedItem().toString();
                boolean usuarioMaster = Boolean.parseBoolean(usuarioMasterString);

                String telefone = jFormattedTextFieldTelefone.getText();
                String endereco = jTextFieldEndereco.getText();
                int numero = Integer.parseInt(jTextFieldNumero.getText());
                String bairro = jTextFieldBairro.getText();
                String cidade = jTextFieldCidade.getText();
                String nome = jTextFieldNome.getText();
                String genero = jComboBoxGenero.getSelectedItem().toString();
                char generoChar = genero != null ? genero.charAt(0) : '\0';
                String dataNascimentoText = jFormattedTextFieldNascimento.getText();
                Date dataNascimento = null;
                try {
                    dataNascimento = formato.parse(dataNascimentoText);

                } catch (ParseException e) {
                    e.printStackTrace();
                    System.out.println("\n======= PROBLEMA NA CONVERSÃO DA DATA DE CADASTRO =========\n");
                }

                String cpf = jFormattedTextFieldCpf.getText();
                String numeroRegistro = jTextFieldNumeroRegistro.getText();
                String observacoes = jTextAreaObservacoes.getText();
                Icon foto = jLabelFoto.getIcon();
                String instagram = jTextFieldInstagram.getText();
                String formacao = jTextFieldFormacao.getText();

                List<Professor> professores = new ArrayList();
                List<Object> professoresGeneric = gerInterGrafica.getGerDom().listar(Professor.class);
                for (Object obj : professoresGeneric) {
                    if (obj instanceof Professor) {
                        professores.add((Professor) obj);
                    }
                }

                for (Professor professorSel : professores) {
                    if (codigo == professorSel.getId()) {
                        professor = professorSel;
                        existente = 1;
                    }
                }

                if (existente == 0) {
                    if (codigoText == null || codigoText.equals("")) {
                        JOptionPane.showMessageDialog(this, "INSIRA UM CODIGO! ");
                    } else {
                        try {

                            int id = gerDominio.inserirProfessor(situacao, numeroRegistro, usuarioMaster, nome, cpf, dataNascimento, endereco, telefone, bairro, generoChar, cidade, observacoes, foto, dataCadastro, senha, codigo, numero, formacao, instagram);

                            setaNull();
                            JOptionPane.showMessageDialog(this, "Professor " + id + " inserido com sucesso.");
                        } catch (HibernateException ex) {
                            JOptionPane.showMessageDialog(this, ex.getMessage());

                        }
                    }

                } else {
                    if (codigoText == null || codigoText.equals("")) {
                        JOptionPane.showMessageDialog(this, "INSIRA UM CODIGO! ");
                    } else {
                        try {

                            int id = gerDominio.alterarProfessor(professor, situacao, numeroRegistro, usuarioMaster, nome, cpf, dataNascimento, endereco, telefone, bairro, generoChar, cidade, observacoes, foto, dataCadastro, senha, codigo, numero, instagram, formacao);
                            setaNull();
                            this.dispose();
                            JOptionPane.showMessageDialog(this, "Professor " + id + " alterado com sucesso.");

                        } catch (HibernateException ex) {
                            JOptionPane.showMessageDialog(this, ex.getMessage());

                        }
                    }
                }
            }

        }


    }//GEN-LAST:event_jButtonConfirmarActionPerformed

